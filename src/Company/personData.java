package Company;

public class personData {
	private String financePhase;
	private String companyType;
	private String companyName;
	private String founderName;
	private String companyDes;
	private String location;
	
	public String getFinancePhase() {
		return financePhase;
	}
	public void setFinancePhase(String financePhase) {
		this.financePhase = financePhase;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getCompanyType() {
		return companyType;
	}
	public void setCompanyType(String companyType) {
		this.companyType = companyType;
	}
	public String getFounderName() {
		return founderName;
	}
	public void setFounderName(String founderName) {
		this.founderName = founderName;
	}
	public String getCompanyDes() {
		return companyDes;
	}
	public void setCompanyDes(String companyDes) {
		this.companyDes = companyDes;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}

}
